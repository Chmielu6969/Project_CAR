/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM4_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_TIM11_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
  LCD_Init();
  LCD_SetCursor(0, 0);
  LCD_Print(lcd_messages[0]);
  LCD_SetCursor(1, 0);
  LCD_Print("M:STOP  S:SRODEK");

  Servo_Init();
  Motor_Init();
  Joystick_Calibrate();
  UartCmd_Init(&huart1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

    /* === SERVO: binary – joystick past threshold → max deflection, else center === */
    {
      float lsx = UartCmd_GetLSX();
      if (lsx > SERVO_STEER_THRESHOLD)
          servo_current_us = SERVO_CENTER_US + SERVO_MAX_DEFLECT_US;
      else if (lsx < -SERVO_STEER_THRESHOLD)
          servo_current_us = SERVO_CENTER_US - SERVO_MAX_DEFLECT_US;
      else
          servo_current_us = SERVO_CENTER_US;
      Servo_SetPulse(servo_current_us);
    }

    /* === WOLNE ZADANIA: joystick, silniki, LCD, CROSS – co 50 ms === */
    loop_tick++;
    if (loop_tick >= SLOW_TASK_TICKS)
    {
      loop_tick = 0;

      JoystickData_t joy;
      Joystick_Read(&joy);

      /* Joystick button: cycle LCD row 0 message (debounced) */
      if (joy.btn_pressed)
      {
        uint32_t now = HAL_GetTick();
        if ((now - joy_last_tick) >= JOY_DEBOUNCE_MS)
        {
          joy_last_tick = now;
          msg_idx = (msg_idx + 1) % LCD_MSG_COUNT;
        }
      }

      /* CROSS → toggle emergency stop (rising edge only) */
      uint8_t cross = UartCmd_GetCross();
      if (cross == 1U && prev_cross == 0U)
      {
        estop_active = !estop_active;
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin,
                          estop_active ? GPIO_PIN_SET : GPIO_PIN_RESET);
      }
      prev_cross = cross;

      /* R2 → przód, L2 → tył, proporcjonalnie; CROSS = e-stop */
      float r2 = UartCmd_GetR2();
      float l2 = UartCmd_GetL2();

      MotorDir_t motor_dir;
      uint16_t   motor_speed;

      if (estop_active)
      {
        motor_dir   = MOTOR_STOP;
        motor_speed = 0U;
      }
      else if (r2 > 0.05f)
      {
        motor_dir   = MOTOR_FORWARD;
        motor_speed = (uint16_t)(r2 * (float)MOTOR_MAX_SPEED);
      }
      else if (l2 > 0.05f)
      {
        motor_dir   = MOTOR_BACKWARD;
        motor_speed = (uint16_t)(l2 * (float)MOTOR_MAX_SPEED);
      }
      else
      {
        motor_dir   = MOTOR_STOP;
        motor_speed = 0U;
      }

      uint8_t lcd_row1_changed = 0;

      if (motor_dir != prev_motor_dir)
      {
        Motor_SetAll(motor_dir, motor_speed);
        prev_motor_dir = motor_dir;
        lcd_row1_changed = 1;
      }
      else if (motor_dir != MOTOR_STOP)
      {
        /* Aktualizuj prędkość nawet gdy kierunek się nie zmienił */
        Motor_SetAll(motor_dir, motor_speed);
      }

      if (servo_current_us != prev_servo_us)
      {
        prev_servo_us = servo_current_us;
        lcd_row1_changed = 1;
      }

      if (msg_idx != prev_msg_idx)
      {
        LCD_SetCursor(0, 0);
        LCD_Print(lcd_messages[msg_idx]);
        prev_msg_idx = msg_idx;
      }

      if (lcd_row1_changed)
      {
        float lsx_snap = UartCmd_GetLSX();
        const char *m_str = estop_active                   ? "M:ESTOP " :
                            (motor_dir == MOTOR_FORWARD)  ? "M:PRZOD " :
                            (motor_dir == MOTOR_BACKWARD) ? "M:TYL   " :
                                                            "M:STOP  ";
        const char *s_str = (lsx_snap < -0.2f) ? "S:LEWO  " :
                            (lsx_snap >  0.2f) ? "S:PRAWO " :
                                                  "S:SRODEK";
        LCD_SetCursor(1, 0);
        LCD_Print(m_str);
        LCD_SetCursor(1, 8);
        LCD_Print(s_str);
      }
    }

    HAL_Delay(10);
  }
  /* USER CODE END 3 */
}

